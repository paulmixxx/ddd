<?php

declare(strict_types=1);

namespace ZenBox\Ddd\Example\Domain\Model;

final class Email
{
    /**
     * @var string
     */
    private $value;

    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }
}